    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {//erro handling
            int i = jTable1.getSelectedRow();
            if (i != -1) {
                long iId = (long) jTable1.getValueAt(i, 0);
                facade.delete(iId);
                updateTable();
            } else {
                JOptionPane.showMessageDialog(this, "Nehuma tarefa selecionada", "Erro", JOptionPane.ERROR_MESSAGE);
            }

        } catch (TarefaExceptions ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int i = jTable1.getSelectedRow();
        long iId = (long) jTable1.getValueAt(i, 0);
        Tarefas selectedTarefa = TarefaInstance.createInstance();
        selectedTarefa = facade.read(iId);

        if (selectedTarefa != null) {
            Tela3 updateDialog = new Tela3(this, selectedTarefa);
            updateDialog.setVisible(true);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

