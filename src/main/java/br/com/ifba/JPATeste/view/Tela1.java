    private void filtrarTable() {
        String searchText = txtBuscar.getText().trim().toLowerCase();

        if (searchText.length() == 0) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + searchText, 1)); // Filter by column 1 (name) case-insensitively
        }
    }

    public void addRowToTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        ArrayList<Tarefas> tarefas = (ArrayList<Tarefas>) facade.readAll();

        Object[] row = new Object[4];

        for (Tarefas tarefa : tarefas) {
            row[0] = tarefa.getId();
            row[1] = tarefa.getDescricao();
            row[2] = tarefa.isFinalizado();
            row[3] = tarefa.getDataFinal().getTime();
            model.addRow(row);
        }
        jTable1.setModel(model);
        repaint();
    }

    public void updateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        addRowToTable();
    }

